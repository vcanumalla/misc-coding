import java.util.*;
public class A {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        System.out.println(n);
        for (int i = 0; i < n - 1; i++) {
            System.out.print(1 + " "  );
        }
        System.out.print(1 + "\n");

    }
}
